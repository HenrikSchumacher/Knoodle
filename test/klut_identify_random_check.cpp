// Resolve (crossing_count, id) -> knot name for human-readable output. FindID's id
// is the knot's 0-based index within crossing-number subtable c, i.e. the id-th
// line of Klut_Values_cc.tsv (verified against FindName). Cached per crossing count.
static std::string NameOf(const std::string& dir, Int c, Klut::ID_T id)
{
    static std::map<Int, std::vector<std::string>> cache;
    auto it = cache.find(c);
    if (it == cache.end())
    {
        std::vector<std::string> names;
        std::ifstream vf(dir + "/Klut_Values_" + (c < 10 ? "0" : "") + std::to_string(c) + ".tsv");
        std::string name; long count;
        while (vf >> name >> count) { names.push_back(name); }
        it = cache.emplace(c, std::move(names)).first;
    }
    return (id < it->second.size()) ? it->second[id] : std::string("<unknown>");
}

