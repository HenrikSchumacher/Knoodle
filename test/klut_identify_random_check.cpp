int main()
{
    const Int n = static_cast<Int>((sizeof(PD) / sizeof(PD[0])) / 5);

    const std::string dir = "../data/Klut";
    Klut klut{ std::filesystem::path(dir), static_cast<Knoodle::Size_T>(13) };
    klut.RequireSubtables();
    ki::Reapr_T reapr{};

    std::vector<Int> code(PD, PD + (sizeof(PD) / sizeof(PD[0])));
    PD_T pd = PD_T::FromSignedPDCode(code.data(), n, false, true);
    PDC_T pdc; pdc.Push(std::move(pd));
    auto res = ki::Identify(klut, std::move(pdc), reapr);

    // Key by (crossing_count, id): FindID's id is an index WITHIN a crossing-number
    // subtable, so the id alone is ambiguous (id 0 at c=3 is a different knot than
    // id 0 at c=4). The (c,id) pair is the unambiguous knot identity.
    std::map<std::pair<Int, Klut::ID_T>, int> ids;
    int unident = 0, err = 0;
    for (const auto& s : res.summands)
    {
        if      (s.kind == ki::Summand::Kind::Identified)   { ++ids[{s.crossings, s.id}]; }
        else if (s.kind == ki::Summand::Kind::Unidentified) { ++unident; }
        else                                                { ++err; }
    }

    std::cout << "Identify(random 2048-gon, " << n << " cx): " << res.summands.size()
              << " summands, " << ids.size() << " distinct identified, "
              << unident << " unidentified, " << err << " error, component_error="
              << res.component_error << ", reapr_calls=" << res.reapr_calls << "\n";
    std::cout << "  identified prime summands (name [c,id] x count):\n";
    for (auto [ci, c] : ids)
        std::cout << "    " << NameOf(dir, ci.first, ci.second)
                  << "  [c=" << ci.first << ",id=" << ci.second << "] x" << c << "\n";

    // Golden decomposition observed 2026-06-17 (stable 10/10). Names (via FindName):
    //   (3,0)=K[3,1,1,"e/r"]  (3,1)=K[3,1,1,"m/mr"]  (4,0)=K[4,1,1,"e/m/r/mr"]
    //   (5,3)=K[5,2,1,"m/mr"] (6,4)=K[6,3,1,"e/m/r/mr"] (9,13)=K[9,7,1,"m/mr"]
    //   + 1 Unidentified (15 cx, out of table range).  0 unknot summands (none arise).
    // A mismatch => Identify changed (regression, or an improvement -> update golden).
    const std::map<std::pair<Int, Klut::ID_T>, int> golden = {
        {{3,0},2}, {{3,1},3}, {{4,0},1}, {{5,3},1}, {{6,4},1}, {{9,13},1}
    };
    const int golden_unidentified = 1;
    const bool pass =
        res.status == ki::IdentifyResult::Status::Knot
        && !res.component_error && err == 0
        && ids == golden && unident == golden_unidentified;
    std::cout << (pass
        ? "PASS: matches golden decomposition (9 identified + 1 unidentified)\n"
        : "*** CHANGED: differs from golden -- regression, or an improvement that should update the golden ***\n");
    return pass ? 0 : 1;
}
